"""
classifier.py — ИИ-классификатор диалогов RetailCRM
Использует Claude API для определения тега по тексту разговора.
"""

import anthropic

TAGS = [
    "запрос на сотрудничество",
    "возврат товара",
    "обмен товара",
    "спам",
    "ждет ответ",
    "новый клиент",
]

SYSTEM_PROMPT = """Ты — ассистент для отдела продаж интернет-магазина.
Твоя задача: проанализировать текст диалога с клиентом и выбрать ОДИН наиболее подходящий тег из списка.

Доступные теги и их значения:
- «запрос на сотрудничество» — клиент хочет стать партнёром, дилером, оптовым покупателем или предлагает деловое сотрудничество
- «возврат товара» — клиент хочет вернуть купленный товар, оформить возврат или получить деньги обратно
- «обмен товара» — клиент хочет обменять товар на другой размер, цвет или модель
- «спам» — сообщение рекламного характера, бессмысленный текст, автоматическая рассылка или подозрительный контент
- «ждет ответ» — клиент задал вопрос или написал сообщение и ожидает ответа менеджера (нет ответа от команды)
- «новый клиент» — клиент впервые обращается, знакомится с ассортиментом, уточняет условия покупки

Правила:
1. Верни ТОЛЬКО название тега из списка выше, без пояснений, кавычек и точки.
2. Если ситуация неоднозначна — выбери наиболее вероятный тег.
3. Если диалог пустой или непонятный — верни «ждет ответ».
"""


def classify_dialog(dialog_text: str, api_key: str) -> str:
    """
    Классифицирует диалог и возвращает подходящий тег.

    Args:
        dialog_text: Текст диалога (сообщения клиента и менеджера)
        api_key: Ключ Anthropic API

    Returns:
        Один из тегов из списка TAGS
    """
    client = anthropic.Anthropic(api_key=api_key)

    message = client.messages.create(
        model="claude-haiku-4-5-20251001",  # быстрая и дешёвая модель для классификации
        max_tokens=50,
        system=SYSTEM_PROMPT,
        messages=[
            {
                "role": "user",
                "content": f"Диалог с клиентом:\n\n{dialog_text}\n\nКакой тег поставить?",
            }
        ],
    )

    raw = message.content[0].text.strip().lower()

    # Нормализация ответа: ищем ближайший тег из списка
    for tag in TAGS:
        if tag in raw:
            return tag

    # Запасной вариант — «ждет ответ»
    return "ждет ответ"
