"""
classifier.py — ИИ-классификатор диалогов RetailCRM
Использует Claude API для определения тегов по тексту разговора.
Поддерживает несколько тегов одновременно.
"""

import anthropic

TAGS = [
    "запрос на сотрудничество",
    "возврат товара",
    "обмен товара",
    "спам",
    "ждет ответ",
    "новый клиент",
]

SYSTEM_PROMPT = """Ты — ассистент для отдела продаж интернет-магазина.
Твоя задача: проанализировать текст диалога с клиентом и выбрать ВСЕ подходящие теги из списка.

Доступные теги и их значения:
- «запрос на сотрудничество» — клиент хочет стать партнёром, дилером, оптовым покупателем или предлагает деловое сотрудничество
- «возврат товара» — клиент хочет вернуть купленный товар, оформить возврат или получить деньги обратно
- «обмен товара» — клиент хочет обменять товар на другой размер, цвет или модель
- «спам» — сообщение рекламного характера, бессмысленный текст, автоматическая рассылка или подозрительный контент
- «ждет ответ» — клиент задал вопрос или написал сообщение и ожидает ответа менеджера (нет ответа от команды)
- «новый клиент» — клиент впервые обращается, знакомится с ассортиментом, уточняет условия покупки

Правила:
1. Верни ТОЛЬКО названия тегов из списка выше, через запятую.
2. Можно вернуть несколько тегов если они подходят (например: «новый клиент, ждет ответ»).
3. Не добавляй пояснений, кавычек или точку в конце.
4. Если диалог пустой или непонятный — верни «ждет ответ».
"""


def classify_dialog(dialog_text: str, api_key: str) -> list[str]:
    """
    Классифицирует диалог и возвращает список подходящих тегов.
    """
    client = anthropic.Anthropic(api_key=api_key)

    message = client.messages.create(
        model="claude-haiku-4-5-20251001",
        max_tokens=100,
        system=SYSTEM_PROMPT,
        messages=[
            {
                "role": "user",
                "content": f"Диалог с клиентом:\n\n{dialog_text}\n\nКакие теги поставить?",
            }
        ],
    )

    raw = message.content[0].text.strip().lower()

    # Parse multiple tags from response
    found_tags = []
    for tag in TAGS:
        if tag in raw:
            found_tags.append(tag)

    # Fallback
    if not found_tags:
        found_tags = ["ждет ответ"]

    return found_tags
